# Function to calculate mean and std deviation
def calculate_stats(model_data):
    stats = {}
    for model, data in model_data.items():
        stats[model] = {
            zeta: {
                'mean': np.mean(ari),
                'std': np.std(ari)
            }
            for zeta, ari in data.items()
        }
    return stats

